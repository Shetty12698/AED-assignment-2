    private void patientRegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_patientRegisterBtnActionPerformed
        // TODO add your handling code here

        Patient pat = new Patient();
        pat.setName(patientName.getText());
        pat.setAge(Integer.parseInt(patientAge.getText()));
        pat.setEmailId(patientEmailID.getText());
        pat.setContactDetails(Long.parseLong(patientContact.getText()));
        pat.setGender((String)patientGenderCombo.getSelectedItem());
        

        pat.residence.communityName = (String)comboCommunity.getSelectedItem();
        pat.residence.houseNumber = (String)comboHouse.getSelectedItem();
        pat.residence.zip = Integer.parseInt(patientZipCode.getText());
        pat.residence.cityName = (String)comboCity.getSelectedItem();
        pat.residence.state = patientState.getText();
        //pat.setAllergies(patientAllergies.getText());
        pat.setPatientId(Integer.parseInt(patientPatientID.getText()));
        pat.setBloodgroup((String)patientBloodGroup.getSelectedItem());
        pat.setPatientheight(Float.parseFloat(patientHeight.getText()));
        pat.setPatientWeight(Float.parseFloat(patientWeight.getText()));
        String pat_pwd = new String(patientPassword.getPassword());
        pat.setPassword(pat_pwd);

        PatientDirectory.patientDir.add(pat);

        JOptionPane.showMessageDialog(this,"Patient Successfully Added");

        resetPatientForm();
        patientDashboardTable();
    }//GEN-LAST:event_patientRegisterBtnActionPerformed

