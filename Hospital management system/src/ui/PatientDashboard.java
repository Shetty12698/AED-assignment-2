    public void patientDashboardTable(){
    
        DefaultTableModel model = (DefaultTableModel) patientDashboardTable.getModel();
        model.setRowCount(0);
        
        System.out.println("Done");
        for(Patient pt:patientDir.getPatientDir()){
            Object[] row=new Object[11];
            row[0]=pt;
            row[1]=pt.getPatientId();
            row[2]=pt.getContactDetails();
            row[3]=pt.getEmailId();
            row[4]=pt.getGender();
            row[5]=pt.getAge();
            row[6]=pt.residence.getHouseNumber() + ", "+ ", " + pt.residence.getCommunityName() + ", " + pt.residence.getCityName() + ", " + pt.residence.getState()+ "," + pt.residence.getZip();
            row[7]=pt.getBloodgroup();
            row[8]=pt.getPatientheight();
            row[9]=pt.getPatientWeight();
            //row[10]=pt.getAllergies();
            model.addRow(row);
                    
        
        }
    }
