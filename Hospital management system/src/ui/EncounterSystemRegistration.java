    public void encounterDisplayTable(){
        DefaultTableModel model1 = (DefaultTableModel) encounterSysTable.getModel();
        model1.setRowCount(0);
        System.out.println("Done");
        for (Encounter es:EncounterHistory.getEncounterHist()){
            Object[] row=new Object[7];
            row[0]=es;
            row[1]=es.getEncounterDate();
            row[2]=es.getPatientId();
            row[3]=es.getName();
            row[4]=es.doctor.getDoctorID();
            row[5]=es.doctor.getName();
            row[6]=es.getEncounterTime();
            model1.addRow(row);
        }
    }
