    private void createEncounterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEncounterActionPerformed
        // TODO add your handling code here:
        
        Encounter enc = new Encounter();
        enc.setEncounterID(Integer.parseInt(EncounterID.getText()));
        enc.setPatientId(Integer.parseInt(PatID.getText()));
        enc.doctor.setDoctorID(Integer.parseInt(DocID.getText()));
        
        enc.setName(patientName.getText());
        
        enc.doctor.setName(EncSysDocTxt.getText());
        EncounterHistory.encounterHist.add(enc);
        JOptionPane.showMessageDialog(this, "Encounter Successfully Added");
        encounterDisplayTable();
    }//GEN-LAST:event_createEncounterActionPerformed

