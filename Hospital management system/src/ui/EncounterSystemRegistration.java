    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        
        int rowIndex = encounterSysTable.getSelectedRow();
        if (rowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) encounterSysTable.getModel();
//        System.out.print(model.getValueAt(rowIndex,0));
        Encounter selectedEncounter =(Encounter)model.getValueAt(rowIndex,0);
        encounterHist.deleteEncounter(selectedEncounter);



        JOptionPane.showMessageDialog(this,"Doctor Deleted");
        encounterDisplayTable();
    }//GEN-LAST:event_DeleteButtonActionPerformed

